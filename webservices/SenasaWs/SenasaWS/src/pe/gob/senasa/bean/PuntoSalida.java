package pe.gob.senasa.bean;

public class PuntoSalida {
    private String codigoPuntoSalida;
    private String puntoSalida;
    private String codigoPais;


    public void setCodigoPuntoSalida(String codigoPuntoSalida) {
        this.codigoPuntoSalida = codigoPuntoSalida;
    }

    public String getCodigoPuntoSalida() {
        return codigoPuntoSalida;
    }

    public void setPuntoSalida(String puntoSalida) {
        this.puntoSalida = puntoSalida;
    }

    public String getPuntoSalida() {
        return puntoSalida;
    }

    public void setCodigoPais(String codigoPais) {
        this.codigoPais = codigoPais;
    }

    public String getCodigoPais() {
        return codigoPais;
    }
}
